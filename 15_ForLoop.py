# Sometimes a programmer wants to execute a group of statements a certain number of times
# This can be done using loops


# For loop syntax
name = "Maha"
for i in name:
    print(i)
print("-------")


# What is range()?
for i in range(4):
    print(i)
print("-------")


# What if we want to print from 2
for j in range(2,5):
    print(j)
print("-------")


# What if we want even numbers
for k in range(2, 11, 2):
    print(k)            # The third argument skips each second number
print("-------")


# We can use any type of command in for statement